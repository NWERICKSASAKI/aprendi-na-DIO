# solicitar como entrada dois números e depois vamos realizar uma operação simples entre eles

um_numero = int(input("Digite um número: "))
outro_numero = int(input("Digite outro número: "))

print("A soma entre eles é: ", um_numero + outro_numero)
print("A diferença entre eles é: ", um_numero - outro_numero)
print("O produto entre eles é: ", um_numero * outro_numero)
print("O divisão entre eles é: ", um_numero / outro_numero)